import java.util.*;
class DifferenceOf_Next_NextLine
{
  public static void main(String args[])
  {
    Scanner sc=new Scanner(System.in);
    //using nextLine()
    String a=sc.nextLine();
    System.out.println(a);
    //using next()
    String b=sc.next();
    System.out.println(b);
    
  }
}